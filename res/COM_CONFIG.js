+"However simple sound production was designed on the Apple II, since 1997, JavaScript maintained <strong>1ms</strong> as the <strong>highest achievable timing accuracy</strong>.  While the 6502 CPU was clocked at approximately 1MHz, we need a 1000-fold more accuracy than JavaScript can provide today.  JavaScript simply cannot implement any timer-driven sound above it's nyquist maximum of 500Hz, while a typical Apple II Beep sound is 1KHz.</p>"
+"<p>W3C recently started to worry about this limitation by proposing a new spec called <a href=\"https://w3c.github.io/hr-time/\">\"High Resolution Time\"</a> or <a href=\"https://w3c.github.io/hr-time/\">hr-time</a>, but because of alleged malicious capability like [CACHE-ATTACKS] and [SPECTRE], W3C recommends to purposefully mess-up it's timers accuracy by reducing resolution, adding jitter, or by any other means that probably never will provide us anything near to 1µs clock accuracy.</p>"
+"<p>Since we may never reach this accuracy, we have to think about how hard we need 1µs clock accuracy for sound emulation after all.  We can toggle the Apple II speaker on and off by reading the $C030 address location as often as we want.  But, knowing that frequencies above 20000Hz remain inaudible, we should mind about toggling the speaker only 40000 times per second = once every 25µs.  The \"coarsen time\" add-on proposed by W3C will by default deliver 100µs accuracy, but by setting the flag crossOriginIsolatedCapability = true, it may deliver 5µs.</p>"
+"<p>In conclusion, we only need a maximum of 25µs timer accuracy, which <a href=\"https://w3c.github.io/hr-time/\">hr-time</a> will likely provide, fingers crossed! </p>"
